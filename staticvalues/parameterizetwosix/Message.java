package constructor.parameterizeconstructor.staticvalues.parameterizetwosix;

public class Message {
    private String from;
    private String body;

    public Message(String from, String body) {
        this.from = from;
        this.body = body;
    }
    public String getFrom() {
        return from;
    }
    public String getBody() {
        return body;
    }
}
