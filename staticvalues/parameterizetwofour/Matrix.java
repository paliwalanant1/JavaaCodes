package constructor.parameterizeconstructor.staticvalues.parameterizetwofour;

public class Matrix {
    private int rows;
    private int cols;

    public Matrix(int rows, int cols) {
        this.rows = rows;
        this.cols = cols;
    }
    public int getRows() {
        return rows;
    }
    public int getCols() {
        return cols;
    }
}
